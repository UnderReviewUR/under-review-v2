/** Lookup by FIFA-style abbreviation (MEX, USA, …). */
export function getWcTeamByAbbr(abbr) {
  const key = String(abbr || "").trim().toUpperCase();
  return WC_2026_TEAMS.find((t) => t.abbreviation === key) || null;
}

/** Teams in a group letter, sorted by Elo (pre-tournament projection). */
export function getWcTeamsByGroup(groupLetter) {
  const g = String(groupLetter || "").trim().toUpperCase();
  return WC_2026_TEAMS.filter((t) => t.group === g).sort((a, b) => b.eloRating - a.eloRating);
}
